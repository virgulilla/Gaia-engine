namespace GaiaEngine.Domain.World;

/// <summary>
/// Defines the supported deterministic soil types used by terrain data.
/// </summary>
public enum SoilType
{
    /// <summary>
    /// Identifies loam soil.
    /// </summary>
    Loam,

    /// <summary>
    /// Identifies clay soil.
    /// </summary>
    Clay,

    /// <summary>
    /// Identifies sandy soil.
    /// </summary>
    Sand,

    /// <summary>
    /// Identifies silty soil.
    /// </summary>
    Silt,

    /// <summary>
    /// Identifies peat soil.
    /// </summary>
    Peat,

    /// <summary>
    /// Identifies rocky ground.
    /// </summary>
    Rock,
}
