namespace GaiaEngine.Domain.World;

/// <summary>
/// Defines the supported deterministic climate zones of the world.
/// </summary>
public enum ClimateZone
{
    /// <summary>
    /// Identifies tropical climate conditions.
    /// </summary>
    Tropical,

    /// <summary>
    /// Identifies temperate climate conditions.
    /// </summary>
    Temperate,

    /// <summary>
    /// Identifies continental climate conditions.
    /// </summary>
    Continental,

    /// <summary>
    /// Identifies polar climate conditions.
    /// </summary>
    Polar,

    /// <summary>
    /// Identifies arid climate conditions.
    /// </summary>
    Arid,
}
