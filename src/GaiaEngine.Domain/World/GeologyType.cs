namespace GaiaEngine.Domain.World;

/// <summary>
/// Defines the supported deterministic geological compositions used by terrain data.
/// </summary>
public enum GeologyType
{
    /// <summary>
    /// Identifies granite geology.
    /// </summary>
    Granite,

    /// <summary>
    /// Identifies limestone geology.
    /// </summary>
    Limestone,

    /// <summary>
    /// Identifies clay geology.
    /// </summary>
    Clay,

    /// <summary>
    /// Identifies volcanic rock geology.
    /// </summary>
    VolcanicRock,
}
