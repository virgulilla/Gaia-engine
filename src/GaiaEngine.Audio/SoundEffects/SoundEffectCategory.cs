namespace GaiaEngine.Audio.SoundEffects;

/// <summary>
/// Defines the supported sound effect categories.
/// </summary>
public enum SoundEffectCategory
{
    /// <summary>
    /// Represents organism sound effects.
    /// </summary>
    Organisms = 0,

    /// <summary>
    /// Represents environment sound effects.
    /// </summary>
    Environment = 1,

    /// <summary>
    /// Represents weather sound effects.
    /// </summary>
    Weather = 2,

    /// <summary>
    /// Represents user-interface sound effects.
    /// </summary>
    UI = 3,

    /// <summary>
    /// Represents gameplay sound effects.
    /// </summary>
    Gameplay = 4,

    /// <summary>
    /// Represents system sound effects.
    /// </summary>
    System = 5,
}
