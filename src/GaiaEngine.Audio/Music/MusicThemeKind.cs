namespace GaiaEngine.Audio.Music;

/// <summary>
/// Defines the supported music theme kinds.
/// </summary>
public enum MusicThemeKind
{
    /// <summary>
    /// Identifies the main theme.
    /// </summary>
    MainTheme = 0,

    /// <summary>
    /// Identifies the default exploration theme.
    /// </summary>
    Exploration = 1,

    /// <summary>
    /// Identifies the discovery theme.
    /// </summary>
    Discovery = 2,

    /// <summary>
    /// Identifies the tension theme.
    /// </summary>
    Tension = 3,

    /// <summary>
    /// Identifies the world-event theme.
    /// </summary>
    WorldEvent = 4,

    /// <summary>
    /// Identifies the menu theme.
    /// </summary>
    Menu = 5,

    /// <summary>
    /// Identifies the credits theme.
    /// </summary>
    Credits = 6,
}
