namespace GaiaEngine.Simulation.Diagnostics;

/// <summary>
/// Defines the official logical names used by deterministic simulation systems.
/// </summary>
public static class SimulationSystemNames
{
    /// <summary>
    /// Gets the logical name of the climate system.
    /// </summary>
    public const string Climate = "Climate";

    /// <summary>
    /// Gets the logical name of the resource system.
    /// </summary>
    public const string Resources = "Resources";

    /// <summary>
    /// Gets the logical name of the water system.
    /// </summary>
    public const string Water = "Water";

    /// <summary>
    /// Gets the logical name of the organism update system.
    /// </summary>
    public const string Organisms = "Organisms";

    /// <summary>
    /// Gets the logical name of the species lifecycle system.
    /// </summary>
    public const string Species = "Species";

    /// <summary>
    /// Gets the logical name of the memory system.
    /// </summary>
    public const string Memory = "Memory";

    /// <summary>
    /// Gets the logical name of the autonomous behaviour system.
    /// </summary>
    public const string AI = "AI";

    /// <summary>
    /// Gets the logical name of the movement interaction system.
    /// </summary>
    public const string Movement = "Movement";

    /// <summary>
    /// Gets the logical name of the feeding interaction system.
    /// </summary>
    public const string Feeding = "Feeding";

    /// <summary>
    /// Gets the logical name of the hydration interaction system.
    /// </summary>
    public const string Hydration = "Hydration";

    /// <summary>
    /// Gets the logical name of the statistics system.
    /// </summary>
    public const string Statistics = "Statistics";
}
