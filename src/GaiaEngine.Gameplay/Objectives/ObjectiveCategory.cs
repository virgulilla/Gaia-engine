namespace GaiaEngine.Gameplay.Objectives;

/// <summary>
/// Defines the supported objective categories.
/// </summary>
public enum ObjectiveCategory
{
    /// <summary>
    /// Represents objectives focused on unlocking discoveries.
    /// </summary>
    Discovery = 0,

    /// <summary>
    /// Represents objectives focused on observing simulation behaviour.
    /// </summary>
    Observation = 1,

    /// <summary>
    /// Represents objectives focused on ecological state.
    /// </summary>
    Ecology = 2,

    /// <summary>
    /// Represents objectives focused on evolutionary events.
    /// </summary>
    Evolution = 3,

    /// <summary>
    /// Represents objectives focused on exploration.
    /// </summary>
    Exploration = 4,

    /// <summary>
    /// Represents objectives focused on mastery.
    /// </summary>
    Mastery = 5,
}
