namespace GaiaEngine.Gameplay.Progression;

/// <summary>
/// Defines the supported progression unlock categories.
/// </summary>
public enum ProgressionUnlockCategory
{
    /// <summary>
    /// Represents unlocks for analysis tools.
    /// </summary>
    AnalysisTools = 0,

    /// <summary>
    /// Represents unlocks for simulation controls.
    /// </summary>
    SimulationControls = 1,

    /// <summary>
    /// Represents unlocks for encyclopedia features.
    /// </summary>
    EncyclopediaFeatures = 2,

    /// <summary>
    /// Represents unlocks for statistics features.
    /// </summary>
    Statistics = 3,

    /// <summary>
    /// Represents unlocks for visualization features.
    /// </summary>
    Visualization = 4,

    /// <summary>
    /// Represents cosmetic reward unlocks.
    /// </summary>
    CosmeticRewards = 5,
}
