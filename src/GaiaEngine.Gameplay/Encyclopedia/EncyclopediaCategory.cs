namespace GaiaEngine.Gameplay.Encyclopedia;

/// <summary>
/// Defines the supported encyclopedia categories available to the player profile.
/// </summary>
public enum EncyclopediaCategory
{
    /// <summary>
    /// Represents species entries.
    /// </summary>
    Species = 0,

    /// <summary>
    /// Represents trait entries.
    /// </summary>
    Traits = 1,

    /// <summary>
    /// Represents biome entries.
    /// </summary>
    Biomes = 2,

    /// <summary>
    /// Represents resource entries.
    /// </summary>
    Resources = 3,

    /// <summary>
    /// Represents climate entries.
    /// </summary>
    Climate = 4,

    /// <summary>
    /// Represents behaviour entries.
    /// </summary>
    Behaviours = 5,

    /// <summary>
    /// Represents evolution entries.
    /// </summary>
    Evolution = 6,

    /// <summary>
    /// Represents world history entries.
    /// </summary>
    WorldHistory = 7,
}
