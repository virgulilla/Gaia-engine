    /// Represents discovery achievements.
    /// </summary>
    Discovery = 0,

    /// <summary>
    /// Represents evolution achievements.
    /// </summary>
    Evolution = 1,

    /// <summary>
    /// Represents ecology achievements.
    /// </summary>
    Ecology = 2,

    /// <summary>
    /// Represents exploration achievements.
    /// </summary>
    Exploration = 3,

    /// <summary>
    /// Represents collection achievements.
    /// </summary>
    Collection = 4,

    /// <summary>
    /// Represents mastery achievements.
    /// </summary>
    Mastery = 5,

    /// <summary>
    /// Represents hidden achievements.
    /// </summary>
    Hidden = 6,
}
