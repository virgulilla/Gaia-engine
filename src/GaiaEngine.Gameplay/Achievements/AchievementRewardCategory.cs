namespace GaiaEngine.Gameplay.Achievements;

/// <summary>
/// Defines the supported achievement reward categories.
/// </summary>
public enum AchievementRewardCategory
{
    /// <summary>
    /// Represents a profile badge reward.
    /// </summary>
    ProfileBadge = 0,

    /// <summary>
    /// Represents a cosmetic unlock reward.
    /// </summary>
    CosmeticUnlock = 1,

    /// <summary>
    /// Represents a statistics entry reward.
    /// </summary>
    StatisticsEntry = 2,

    /// <summary>
    /// Represents an encyclopedia decoration reward.
    /// </summary>
    EncyclopediaDecoration = 3,
}
